export function findNearestStation(
  lat: number,
  lng: number,
): Station {
  let best = STATIONS[0]
  let bestDist = Infinity
  for (const s of STATIONS) {
    const dLat = s.lat - lat
    const dLng = (s.lng - lng) * Math.cos((lat * Math.PI) / 180)
    const dist = dLat * dLat + dLng * dLng
    if (dist < bestDist) {
      bestDist = dist
      best = s
    }
  }
  return best
}
